cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort()
print(cars)

# reverse sort
cars.sort(reverse=True)
print(cars)

# sorted function which lets you display list in a particular order but doesn't affect list itself
print(cars)
print(sorted(cars))
print(cars)

# reverse the orders of elements in a list
cars.reverse()
print(cars)
